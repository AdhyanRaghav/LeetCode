class Solution {
public:
    void nextPermutation(vector<int>& nums) {
        int b=-1;

        for(int i=nums.size()-2;i>=0;i--)
        {
            if(nums[i]<nums[i+1]){
                b=i;
                break;
            }
        }

        if(b ==-1){
            reverse(nums.begin(), nums.end());
            return;
        }

        for(int j=nums.size()-1;j>b;j--){
            if(nums[j]>nums[b]){
               swap(nums[j],nums[b]);
               break;
            }
        }

        reverse(nums.begin()+b+1,nums.end());

    }
};